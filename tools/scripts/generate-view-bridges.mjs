// File: tools/scripts/generate-view-bridges.mjs
//
// SSA Bridge Auto-Script
// ----------------------
// Creates flat ".view.jsx" bridge re-export files for calculators that live in
// folder-based feature dirs, e.g.
//
//   src/features/calculators/gardenAnimal/AnimalFeedCalculator/AnimalFeedCalculator.view.jsx
// -> creates:
//   src/features/calculators/gardenAnimal/AnimalFeedCalculator.view.jsx
//
// Usage:
//   node tools/scripts/generate-view-bridges.mjs
//   node tools/scripts/generate-view-bridges.mjs --dry-run
//   node tools/scripts/generate-view-bridges.mjs --force
//   node tools/scripts/generate-view-bridges.mjs --root src/features/calculators
//
// Notes:
// - Default root: src/features/calculators
// - Only targets pattern: <Category>/<CalculatorFolder>/<CalculatorFolder>.view.jsx
// - Skips existing bridges unless --force
// - Writes POSIX-style import paths for Vite compatibility

import fs from "node:fs/promises";
import path from "node:path";

const argv = process.argv.slice(2);

function argValue(flag, fallback = null) {
  const idx = argv.indexOf(flag);
  if (idx === -1) return fallback;
  const v = argv[idx + 1];
  if (!v || v.startsWith("--")) return fallback;
  return v;
}

const DRY_RUN = argv.includes("--dry-run");
const FORCE = argv.includes("--force");
const ROOT = argValue("--root", path.join("src", "features", "calculators"));

const ROOT_ABS = path.resolve(process.cwd(), ROOT);

const HEADER = `// Bridge re-export for Vite build compatibility.
// Auto-generated by tools/scripts/generate-view-bridges.mjs
// DO NOT EDIT by hand (edit the underlying view instead).
`;

function toPosix(p) {
  return p.split(path.sep).join("/");
}

async function exists(p) {
  try {
    await fs.access(p);
    return true;
  } catch {
    return false;
  }
}

async function listDirs(dirAbs) {
  const entries = await fs.readdir(dirAbs, { withFileTypes: true });
  return entries.filter((e) => e.isDirectory()).map((e) => e.name);
}

function makeBridgeContents(categoryName, calculatorName) {
  const relTarget = `./${calculatorName}/${calculatorName}.view.jsx`;
  return `${HEADER}
// Supports imports like:
//   "@/features/calculators/${categoryName}/${calculatorName}.view"
//
// Actual implementation lives at:
//   "${relTarget}"

export { default } from "${relTarget}";
`;
}

async function writeFileSafe(fileAbs, content) {
  const dir = path.dirname(fileAbs);
  await fs.mkdir(dir, { recursive: true });

  if (!FORCE && (await exists(fileAbs))) return { status: "skipped_exists" };

  if (DRY_RUN) return { status: "dry_run" };

  await fs.writeFile(fileAbs, content, "utf8");
  return { status: "written" };
}

async function main() {
  if (!(await exists(ROOT_ABS))) {
    console.error(`[view-bridges] Root folder not found: ${ROOT_ABS}`);
    process.exitCode = 1;
    return;
  }

  const categoryDirs = await listDirs(ROOT_ABS);

  let scanned = 0;
  let candidates = 0;
  let written = 0;
  let skipped = 0;
  let dry = 0;
  let missing = 0;

  const actions = [];

  for (const categoryName of categoryDirs) {
    const categoryAbs = path.join(ROOT_ABS, categoryName);

    // calculators are folders inside the category folder
    const calcDirs = await listDirs(categoryAbs);

    for (const calculatorName of calcDirs) {
      scanned++;

      const calcAbs = path.join(categoryAbs, calculatorName);

      // Expected view file inside the calculator folder
      const viewInsideAbs = path.join(calcAbs, `${calculatorName}.view.jsx`);

      if (!(await exists(viewInsideAbs))) {
        // Not a match for this convention; skip quietly
        missing++;
        continue;
      }

      candidates++;

      // Bridge file sits in category folder
      const bridgeAbs = path.join(categoryAbs, `${calculatorName}.view.jsx`);

      const content = makeBridgeContents(categoryName, calculatorName);
      const result = await writeFileSafe(bridgeAbs, content);

      actions.push({
        category: categoryName,
        calculator: calculatorName,
        bridge: toPosix(path.relative(process.cwd(), bridgeAbs)),
        target: toPosix(path.relative(process.cwd(), viewInsideAbs)),
        status: result.status,
      });

      if (result.status === "written") written++;
      else if (result.status === "skipped_exists") skipped++;
      else if (result.status === "dry_run") dry++;
    }
  }

  // Pretty summary
  console.log("");
  console.log(
    `[view-bridges] root: ${toPosix(path.relative(process.cwd(), ROOT_ABS))}`
  );
  console.log(`[view-bridges] scanned folders: ${scanned}`);
  console.log(`[view-bridges] view matches:    ${candidates}`);
  console.log(`[view-bridges] written:         ${written}`);
  console.log(`[view-bridges] skipped exists:  ${skipped}`);
  console.log(`[view-bridges] dry-run:         ${dry}`);
  console.log(`[view-bridges] folders w/o view:${missing}`);
  console.log("");

  // Show a compact list of what happened (only first 200 to avoid spam)
  const maxList = 200;
  const list = actions.slice(0, maxList);
  for (const a of list) {
    const tag =
      a.status === "written"
        ? "WRITE "
        : a.status === "skipped_exists"
        ? "SKIP  "
        : "DRY   ";
    console.log(`${tag} ${a.bridge}  ->  ${a.target}`);
  }
  if (actions.length > maxList) {
    console.log(`... (${actions.length - maxList} more not shown)`);
  }

  // Exit code for CI friendliness
  if (DRY_RUN) {
    console.log("");
    console.log("[view-bridges] dry-run complete (no files written).");
  } else {
    console.log("");
    console.log("[view-bridges] done.");
  }
}

main().catch((err) => {
  console.error("[view-bridges] fatal:", err);
  process.exitCode = 1;
});
